void solve() {
	LL x, y, a, b;
	inl2(x, y);
	inl2(a, b);

	if (a == b) {
		finish(min(x, y) / a);
	}

	LL low = 0, high = (x + y) / (a + b), ans = 0;
	if (a < b) swap(a, b);

	while (low <= high) {
		LL mid = (low + high) >> 1;

		LL l = ceil((mid * a - y * 1.0) / (a - b));
		LL r = (x - mid * b) / (a - b);

		if (r >= l && r >= 0 && x >= mid * b && y >= mid * b) {
			ans = mid;
			low = mid + 1;
		} else high = mid - 1;
	}

	cout << ans << en;
}

